const http = require('http');

console.log('üîç Testing if Book Bite server is running...\n');

// Test health endpoint
const options = {
  hostname: 'localhost',
  port: 3000,
  path: '/health',
  method: 'GET',
  timeout: 5000
};

const req = http.request(options, (res) => {
  let data = '';
  
  res.on('data', (chunk) => {
    data += chunk;
  });
  
  res.on('end', () => {
    console.log('‚úÖ Server is running!');
    console.log(`üìä Status: ${res.statusCode}`);
    console.log(`üìã Response:`, JSON.parse(data));
    console.log('\nüéâ Your Book Bite backend is working perfectly!');
    console.log('\nüìñ Available endpoints:');
    console.log('   üè• Health Check: http://localhost:3000/health');
    console.log('   üë§ Register User: POST http://localhost:3000/api/v1/auth/register');
    console.log('   üîê Login: POST http://localhost:3000/api/v1/auth/login');
    console.log('   üè® Hotels: GET http://localhost:3000/api/v1/hotels');
    console.log('   üçΩÔ∏è  Restaurants: GET http://localhost:3000/api/v1/restaurants');
    console.log('\nüí° Tip: Use Postman or curl to test these endpoints!');
  });
});

req.on('error', (error) => {
  if (error.code === 'ECONNREFUSED') {
    console.log('‚ùå Server is not running!');
    console.log('\nüöÄ To start your server, run:');
    console.log('   npm run dev');
    console.log('\n‚è≥ Then run this test again.');
  } else {
    console.log('‚ùå Error:', error.message);
  }
});

req.on('timeout', () => {
  console.log('‚è∞ Request timed out - server might be starting up');
  req.destroy();
});

req.end();